void print(int x, int y, color_t fg, color_t bg, const char *text)
{
  int i, j;
  char code;
  unsigned int element;
  const char *pointer;
  color_t *vram = GetVRAMAddress();
  for(j = 0; j < 16; ++j) vram[(y + j) * LCD_WIDTH_PX + x + i] = bg;
  x += 1;
  for(pointer = text; code = *pointer; ++pointer)
  {
    for(i = 0; i < 8; ++i)
    {
      element = font8x16[code * 8 + i];
      for(j = 0; j < 16; ++j)
      {
        vram[(y + j) * LCD_WIDTH_PX + x + i] = element & (1 << j) ? fg : bg;
      }
    }
    for(j = 0; j < 16; ++j) vram[(y + j) * LCD_WIDTH_PX + x + i] = bg;
    x += 9;
  }
}
